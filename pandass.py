import pandas as pd

# Создание DataFrame из словаря
data = {
    'Имя': ['Алиса', 'Боб', 'Чарли'],
    'Возраст': [25, 30, 35],
    'Город': ['Москва', 'Питер', 'Новосибирск']
}
df = pd.DataFrame(data)

# Вывод первых двух строк DataFrame
print(df.head(2))

# Фильтрация данных
filtered_df = df[df['Возраст'] > 28]

# Вывод отфильтрованных данных
print(filtered_df)

