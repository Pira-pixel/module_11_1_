import matplotlib.pyplot as plt

# Создание данных для графика
x = [1, 2, 3, 4, 5]
y = [2, 3, 5, 7, 11]

# Построение графика
plt.plot(x, y, marker='o')

# Настройка заголовка и меток осей
plt.title("Пример графика")
plt.xlabel("X-axis")
plt.ylabel("Y-axis")

# Отображение графика
plt.show()

